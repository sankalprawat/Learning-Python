# Example: Formatting a message with special characters using escape characters

# Define variables
name = "John"
age = 25
city = "New York"

# Create a formatted message with special characters
message = f"Hello, my name is {name}. I'm {age} years old and I live in {city}.\n\
I'm learning Python programming and it's really exciting! Let's learn more about escape characters:\n\
\t- \\n : New line\n\
\t- \\t : Tab\n\
\t- \\\" : Double quote\n\
\t- \\' : Single quote\n\
\t- \\\\ : Backslash\n\
I hope you find this helpful."

# Print the formatted message
print(message)