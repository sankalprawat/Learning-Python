# WAP in python to demonstrate different arithemetic operations on numbers

# Taking input from the user
number1 = int(input("Enter the first number : "))
number2 = int(input("Enter the second number : "))

# Outputting the results
print("Addition operation results : ", number1 + number2)   # Addition
print("Division operation results : ", number1 / number2)   # Division
print("Subtraction operation results : ", number1 - number2)  # Subtraction
print("Multiplication operation results : ", number1 * number2)   # Multiplication
print("Modulus or Remainder operation results : ", number1 % number2)  # Modulus or Remainder